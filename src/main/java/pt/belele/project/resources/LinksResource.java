package pt.belele.project.resources;

public class LinksResource
{
	private Link self;

	private Link teams;

	private Link fixtures;

	private Link leagueTable;

	private Link team;

	private Link soccerseason;

	private Link homeTeam;

	private Link awayTeam;

	private Link players;

	public LinksResource()
	{

	}

	public Link getSelf()
	{
		return self;
	}

	public void setSelf(Link self)
	{
		this.self = self;
	}

	public Link getTeams()
	{
		return teams;
	}

	public void setTeams(Link teams)
	{
		this.teams = teams;
	}

	public Link getFixtures()
	{
		return fixtures;
	}

	public void setFixtures(Link fixtures)
	{
		this.fixtures = fixtures;
	}

	public Link getLeagueTable()
	{
		return leagueTable;
	}

	public void setLeagueTable(Link leagueTable)
	{
		this.leagueTable = leagueTable;
	}

	public Link getTeam()
	{
		return team;
	}

	public void setTeam(Link team)
	{
		this.team = team;
	}

	public Link getSoccerseason()
	{
		return soccerseason;
	}

	public void setSoccerseason(Link soccerseason)
	{
		this.soccerseason = soccerseason;
	}

	public Link getHomeTeam()
	{
		return homeTeam;
	}

	public void setHomeTeam(Link homeTeam)
	{
		this.homeTeam = homeTeam;
	}

	public Link getAwayTeam()
	{
		return awayTeam;
	}

	public void setAwayTeam(Link awayTeam)
	{
		this.awayTeam = awayTeam;
	}

	public Link getPlayers()
	{
		return players;
	}

	public void setPlayers(Link players)
	{
		this.players = players;
	}

}
